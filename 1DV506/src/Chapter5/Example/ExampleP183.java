package Chapter5.Example;

public class ExampleP183 {
    public static void main(String[] args) {
        // Loop
        int sum = 0, i = 1;
        while (i < 10) {
            sum = sum + i;
            i++;
        }
        System.out.println("Sum is " + sum);
        // sum = 1+2+3+ ... +8+9
    }
}
